package org.iplass.mtp.impl.entity;

import org.iplass.mtp.impl.metadata.MetaData;
import org.iplass.mtp.impl.util.ObjectUtil;

public class MetaRdbColumnMapping implements MetaData {
	private static final long serialVersionUID = -7986441656632308617L;

	private String propertyId;
	private String columnName;
	
	public MetaRdbColumnMapping() {
	}
	
	public MetaRdbColumnMapping(String propertyId, String columnName) {
		this.propertyId = propertyId;
		this.columnName = columnName;
	}

	@Override
	public MetaRdbColumnMapping copy() {
		return ObjectUtil.deepCopy(this);
	}

	public String getPropertyId() {
		return propertyId;
	}
	public void setPropertyId(String propertyId) {
		this.propertyId = propertyId;
	}
	public String getColumnName() {
		return columnName;
	}
	public void setColumnName(String columnName) {
		this.columnName = columnName;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result
				+ ((columnName == null) ? 0 : columnName.hashCode());
		result = prime * result
				+ ((propertyId == null) ? 0 : propertyId.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		MetaRdbColumnMapping other = (MetaRdbColumnMapping) obj;
		if (columnName == null) {
			if (other.columnName != null)
				return false;
		} else if (!columnName.equals(other.columnName))
			return false;
		if (propertyId == null) {
			if (other.propertyId != null)
				return false;
		} else if (!propertyId.equals(other.propertyId))
			return false;
		return true;
	}

}
