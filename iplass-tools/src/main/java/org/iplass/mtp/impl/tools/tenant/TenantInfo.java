package org.iplass.mtp.impl.tools.tenant;

import java.sql.Date;
import java.sql.Timestamp;

public class TenantInfo {

	private int id;

	private String name;

	private String description;

	private String hostName;

	private String url;

	private Date yukoDateFrom;

	private Date yukoDateTo;

	private String createUser;

	private Timestamp createDate;

	private String updateUser;

	private Timestamp updateDate;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public String getHostName() {
		return hostName;
	}

	public void setHostName(String hostName) {
		this.hostName = hostName;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public Date getYukoDateFrom() {
		return yukoDateFrom;
	}

	public void setYukoDateFrom(Date yukoDateFrom) {
		this.yukoDateFrom = yukoDateFrom;
	}

	public Date getYukoDateTo() {
		return yukoDateTo;
	}

	public void setYukoDateTo(Date yukoDateTo) {
		this.yukoDateTo = yukoDateTo;
	}

	public String getCreateUser() {
		return createUser;
	}

	public void setCreateUser(String createUser) {
		this.createUser = createUser;
	}

	public Timestamp getCreateDate() {
		return createDate;
	}

	public void setCreateDate(Timestamp createDate) {
		this.createDate = createDate;
	}

	public String getUpdateUser() {
		return updateUser;
	}

	public void setUpdateUser(String updateUser) {
		this.updateUser = updateUser;
	}

	public Timestamp getUpdateDate() {
		return updateDate;
	}

	public void setUpdateDate(Timestamp updateDate) {
		this.updateDate = updateDate;
	}
}
